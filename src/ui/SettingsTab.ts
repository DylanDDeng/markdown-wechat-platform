import { App, PluginSettingTab, Setting } from 'obsidian'
import type WeChatPreviewPlugin from '../main'
import type { FixedTemplateId } from '../templates/fixedTemplates'

export type RenderMode = 'ai' | 'fixed-template'

export type WeChatPreviewSettings = {
  renderMode: RenderMode
  fixedTemplateId: FixedTemplateId
  customCss: string
  openRouterApiKey: string
  openRouterModel: string
  promptThemeId: string
  autoGenerateEnabled: boolean
  autoGenerateDebounceMs: number
}

export const DEFAULT_SETTINGS: WeChatPreviewSettings = {
  renderMode: 'ai',
  fixedTemplateId: 'business-blue',
  customCss: '',
  openRouterApiKey: '',
  openRouterModel: 'google/gemini-3.1-pro-preview',
  promptThemeId: 'digital-tool-guide',
  autoGenerateEnabled: false,
  autoGenerateDebounceMs: 1500,
}

const CUSTOM_MODEL_OPTION = '__custom__'

const OPENROUTER_MODEL_OPTIONS = [
  {
    id: 'google/gemini-3.1-pro-preview',
    label: 'Gemini 3.1 Pro Preview',
  },
  {
    id: 'anthropic/claude-sonnet-4.6',
    label: 'Claude Sonnet 4.6',
  },
  {
    id: 'anthropic/claude-opus-4.6',
    label: 'Claude Opus 4.6',
  },
  {
    id: 'moonshotai/kimi-k2.5',
    label: 'Kimi K2.5',
  },
] as const

function getPresetModelValue(modelId: string): string {
  return OPENROUTER_MODEL_OPTIONS.some((option) => option.id === modelId) ? modelId : CUSTOM_MODEL_OPTION
}

export class WeChatPreviewSettingTab extends PluginSettingTab {
  private plugin: WeChatPreviewPlugin

  constructor(app: App, plugin: WeChatPreviewPlugin) {
    super(app, plugin)
    this.plugin = plugin
  }

  display(): void {
    const { containerEl } = this
    containerEl.empty()

    containerEl.createEl('h2', { text: 'WeChat Preview 高级设置' })

    const noticeEl = containerEl.createDiv({ cls: 'wechatPreviewSettingsNotice' })
    noticeEl.createEl('strong', { text: '主流程已移到预览页' })
    noticeEl.createEl('p', {
      text: '模式切换、AI 主题选择和固定模板选择，现在都在 WeChat Preview 面板里完成。这里保留 API、模型、自动生成和自定义 CSS 等低频配置。',
    })

    new Setting(containerEl)
      .setName('OpenRouter API Key')
      .setDesc('仅在 AI 排版模式下使用，保存在本地插件设置中。')
      .addText((text) => {
        text.inputEl.type = 'password'
        text.setPlaceholder('sk-or-v1-...')
        text.setValue(this.plugin.settings.openRouterApiKey)
        text.onChange(async (value) => {
          this.plugin.settings.openRouterApiKey = value.trim()
          await this.plugin.saveSettings()
        })
      })

    new Setting(containerEl)
      .setName('推荐模型')
      .setDesc('快速选择常用 OpenRouter 模型，也可以继续在下方手动输入任意模型 ID。')
      .addDropdown((dropdown) => {
        dropdown.addOption(CUSTOM_MODEL_OPTION, '自定义 / 当前值')
        OPENROUTER_MODEL_OPTIONS.forEach((option) => {
          dropdown.addOption(option.id, `${option.label} (${option.id})`)
        })
        dropdown.setValue(getPresetModelValue(this.plugin.settings.openRouterModel))
        dropdown.onChange(async (value) => {
          if (value === CUSTOM_MODEL_OPTION) return
          this.plugin.settings.openRouterModel = value
          await this.plugin.saveSettings()
          this.display()
        })
      })

    new Setting(containerEl)
      .setName('Model')
      .setDesc('实际使用的 OpenRouter 模型 ID。可直接填写任意可用 slug。')
      .addText((text) => {
        text.setPlaceholder('google/gemini-3.1-pro-preview')
        text.setValue(this.plugin.settings.openRouterModel)
        text.onChange(async (value) => {
          this.plugin.settings.openRouterModel = value.trim()
          await this.plugin.saveSettings()
        })
      })

    new Setting(containerEl)
      .setName('自动生成 AI HTML')
      .setDesc('仅在 AI 排版模式下生效。切换笔记或停止输入后会自动触发生成。')
      .addToggle((toggle) => {
        toggle.setValue(this.plugin.settings.autoGenerateEnabled)
        toggle.onChange(async (value) => {
          this.plugin.settings.autoGenerateEnabled = value
          await this.plugin.saveSettings()
        })
      })

    new Setting(containerEl)
      .setName('自动生成行为')
      .setDesc(
        `当前 debounce 固定为 ${this.plugin.settings.autoGenerateDebounceMs}ms，并且切换笔记也会触发一次自动生成。`,
      )

    containerEl.createEl('h3', { text: '固定模板高级项' })

    new Setting(containerEl)
      .setName('Custom CSS')
      .setDesc('可选，注入到固定模板输出结果中的额外 CSS。')
      .addTextArea((text) => {
        text.setPlaceholder('.my-title { letter-spacing: 0.2px; }')
        text.setValue(this.plugin.settings.customCss)
        text.inputEl.rows = 6
        text.inputEl.cols = 40
        text.onChange(async (value) => {
          this.plugin.settings.customCss = value
          await this.plugin.saveSettings()
        })
      })

    containerEl.createEl('p', {
      cls: 'wechatPreviewSettingsHint',
      text: '主题和固定模板的具体选择项已经移动到预览工具栏，便于在生成时就地切换。',
    })
  }
}
